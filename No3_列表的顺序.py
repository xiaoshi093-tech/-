#关于旅游景点地区的列表排序代码
places =["beijing","shanghai","shanxi","guangzhou","shenzhen"]
print("原始列表：",places)
#使得旅游景点可以按照字母顺序排序
print(sorted(places))
print(places)
print(sorted(places,reverse=True))
print(places)
places.reverse()
print(places)
places.sort
print(places)
places.sort(reverse=True)
print(places)